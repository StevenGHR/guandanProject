// CguandanClientApp 初始化

BOOL CguandanClientApp::InitInstance()
{
	// 如果一个运行在 Windows XP 上的应用程序清单指定要
	// 使用 ComCtl32.dll 版本 6 或更高版本来启用可视化方式，
	//则需要 InitCommonControlsEx()。否则，将无法创建窗口。
	INITCOMMONCONTROLSEX InitCtrls;
	InitCtrls.dwSize = sizeof(InitCtrls);
	// 将它设置为包括所有要在应用程序中使用的
	// 公共控件类。
	InitCtrls.dwICC = ICC_WIN95_CLASSES;
	InitCommonControlsEx(&InitCtrls);

	CWinApp::InitInstance();


	AfxEnableControlContainer();

	// 创建 shell 管理器，以防对话框包含
	// 任何 shell 树视图控件或 shell 列表视图控件。
	CShellManager *pShellManager = new CShellManager;

	// 标准初始化
	// 如果未使用这些功能并希望减小
	// 最终可执行文件的大小，则应移除下列
	// 不需要的特定初始化例程
	// 更改用于存储设置的注册表项
	// TODO: 应适当修改该字符串，
	// 例如修改为公司或组织名
	SetRegistryKey(_T("应用程序向导生成的本地应用程序"));
	
	//初始化Socket
	if(!AfxSocketInit())
	{
		AfxMessageBox(_T("初始化Socket库失败！"));
		return false;
	}

	m_pSocket = new CClientSocket();
	if(!m_pSocket)
	{
		AfxMessageBox(_T("内存不足！"));
		return false;
	}

	if(!m_pSocket->Create())
	{
		AfxMessageBox(_T("创建套接字失败！"));
		return false;
	}

	CLogoInDlg* pLogoinDlg;
	pLogoinDlg = new CLogoInDlg();
	
	if(pLogoinDlg->DoModal()==IDOK)
	{
		//不登录
		delete pLogoinDlg;
		m_pSocket->Close();
		return false;
	}
	else
	{
		delete pLogoinDlg;
	}

	CguandanClientDlg dlg;
	m_pMainWnd = &dlg;
	INT_PTR nResponse = dlg.DoModal();
	if (nResponse == IDOK)
	{
		// TODO: 在此放置处理何时用
		//  “确定”来关闭对话框的代码
	}
	else if (nResponse == IDCANCEL)
	{
		// TODO: 在此放置处理何时用
		//  “取消”来关闭对话框的代码
	}

	// 删除上面创建的 shell 管理器。
	if (pShellManager != NULL)
	{
		delete pShellManager;
	}

	// 由于对话框已关闭，所以将返回 FALSE 以便退出应用程序，
	//  而不是启动应用程序的消息泵。
	return FALSE;
}

int CguandanClientApp::ExitInstance()
{
	if(m_pSocket)
	{
		delete m_pSocket;
		m_pSocket = NULL;
	}

	return CWinApp::ExitInstance();
}

CClientSocket* CguandanClientApp::GetMainSocket() const
{
	return m_pSocket;
}
